#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*s2;
	int		i;

	s2 = (char *)malloc(ft_strlen(s) + 1);
	if (s2 == NULL)
		return (0);
	i = 0;
	while (s[i])
	{
		s2[i] = s[i];
		i++;
	}
	s2[i] = '\0';
	return ((char *)s2);
}
